fun main() {
    print("House :")
    var n = readln().toString()
    if (n == "gryfffindor"){
        println("bravery")
    }
    else if (n == "hufflepuff"){
        println("loyalty")
    }
    else if (n == "slytherin"){
        println("cunning")
    }
    else if (n == "ravenclaw"){
        println("intellect")
    }
    else{
        println("not valid house")
    }
}